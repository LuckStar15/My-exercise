package Poli;

public class Calc {
	
	public Calc() {
		
	}
	public double somar(double valor1, double valor2) {
		return valor1 + valor2;
	}
	public double somar(int valor1, double valor2) {
		return valor1 + valor2;
	}
	public double somar(double valor1, double valor2, double valor3) {
		return valor1 + valor2 + valor3;
		
	}
}
